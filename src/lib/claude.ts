function fixIcons(result: ResultData): ResultData {
  const iconMap: Record<string, string> = {
    star: "🌟", light: "💡", team: "🤝", fire: "🔥",
    search: "🔍", rocket: "🚀", globe: "🌐", bag: "💼",
    book: "📚", brain: "🧠", heart: "❤️", target: "🎯",
    chart: "📊", pen: "✍️", shield: "🛡️", crown: "👑",
  };
  return {
    ...result,
    strengths: result.strengths.map((s) => ({
      ...s,
      icon: iconMap[s.icon] ?? s.icon ?? "⭐",
    })),
  };
}

export async function generateResult(
  formType: FormType,
  answers: Answer[]
): Promise<ResultData> {
  const apiKey = process.env.ANTHROPIC_API_KEY;

  if (!apiKey) {
    return getFallbackResult(formType, answers);
  }

  const response = await fetch("https://api.anthropic.com/v1/messages", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "x-api-key": apiKey,
      "anthropic-version": "2023-06-01",
    },
    body: JSON.stringify({
      model: "claude-sonnet-4-20250514",
      max_tokens: 4000,
      messages: [{ role: "user", content: buildPrompt(formType, answers) }],
    }),
  });

  if (!response.ok) {
    const errText = await response.text();
    console.error("Claude API error body:", errText);
    throw new Error(`Claude API error: ${response.status}`);
  }

  const data = await response.json();
  const raw: string = data.content?.[0]?.text ?? "";

  console.log("Claude raw response:", raw.slice(0, 300));

  let jsonStr = raw.trim();
  jsonStr = jsonStr.replace(/```json\s*/g, "").replace(/```\s*/g, "").trim();

  const start = jsonStr.indexOf("{");
  const end = jsonStr.lastIndexOf("}");
  if (start === -1 || end === -1) {
    console.error("No JSON found in response:", raw);
    throw new Error("Claude API returned invalid JSON");
  }

  jsonStr = jsonStr.slice(start, end + 1);

  try {
    const result = JSON.parse(jsonStr) as ResultData;
    return fixIcons(result);
  } catch (e) {
    console.error("JSON parse error:", e);
    console.error("JSON string:", jsonStr.slice(0, 500));
    throw new Error("Failed to parse Claude response as JSON");
  }
}

